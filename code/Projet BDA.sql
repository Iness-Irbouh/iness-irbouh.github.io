# Phase III.a

CREATE TABLE Region (
    Code_region VARCHAR(5) NOT NULL,
    Libelle_region VARCHAR(100) NOT NULL,
    PRIMARY KEY (Code_region)
);

CREATE TABLE Academie (
    Code_academie VARCHAR(50) NOT NULL,
    Libelle_academie VARCHAR(100),
    Code_region VARCHAR(5),
    PRIMARY KEY (Code_academie),
    FOREIGN KEY (Code_region) REFERENCES Region(Code_region)
);



CREATE TABLE Departement (
    Code_departement VARCHAR(5) NOT NULL,
    Libelle_departement VARCHAR(100),
    Code_academie VARCHAR(50) NOT NULL,
    PRIMARY KEY (Code_departement),
    FOREIGN KEY (Code_academie) REFERENCES Academie(Code_academie)
);

CREATE TABLE Commune (
    Code_commune VARCHAR(10) NOT NULL,
    Nom_commune VARCHAR(150),
    Code_departement VARCHAR(5) NOT NULL,
    PRIMARY KEY (Code_commune),
    FOREIGN KEY (Code_departement) REFERENCES Departement(Code_departement)
);

CREATE TABLE Etablissement (
    UAI VARCHAR(20) NOT NULL,
    Nom_etablissement VARCHAR(300),
    Type_lycee VARCHAR(50),
    Secteur ENUM('public', 'prive'),
    Code_commune VARCHAR(10) NOT NULL,
    PRIMARY KEY (UAI),
    FOREIGN KEY (Code_commune) REFERENCES Commune(Code_commune)
);

CREATE TABLE IPS (
    ID_IPS INT AUTO_INCREMENT NOT NULL,
    UAI VARCHAR(20) NOT NULL,
    Annee_scolaire VARCHAR(10) NOT NULL,
    IPS_GT_PRO_Ensemble DECIMAL(5,2),
    PRIMARY KEY (ID_IPS),
    FOREIGN KEY (UAI) REFERENCES Etablissement(UAI)
);

ALTER TABLE IPS
ADD CONSTRAINT uq_ips_uai_annee
UNIQUE (UAI, Annee_scolaire);

CREATE TABLE Resultat_Bac (
    ID_Resultat INT AUTO_INCREMENT NOT NULL,
    UAI VARCHAR(10) NOT NULL,
    Annee INT,
    Presents_Toutes_Series INT,
    Taux_reussite_Toutes_Series DECIMAL(5, 2),
    Taux_mentions_Toutes_Series DECIMAL(5, 2),
    Valeur_Ajoutee_Taux_Reussite DECIMAL(5, 2),
    Valeur_Ajoutee_Taux_Mentions DECIMAL(5, 2),
    Taux_acces_2nde_bac DECIMAL(5, 2),
    Valeur_Ajoutee_Taux_Acces_2nde_bac DECIMAL(5, 2),
    Taux_reussite_L DECIMAL(5, 2),
    Taux_reussite_ES DECIMAL(5, 2),
    Taux_reussite_S DECIMAL(5, 2),
    Taux_reussite_STMG DECIMAL(5, 2),
    PRIMARY KEY (ID_Resultat),
    UNIQUE KEY (UAI, Annee),
    FOREIGN KEY (UAI) REFERENCES Etablissement(UAI)
);

